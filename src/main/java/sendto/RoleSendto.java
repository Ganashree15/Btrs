package sendto;

public class RoleSendto {

	private Long id;
	private String value;

	public RoleSendto() {
	}

	public RoleSendto(Long id) {
		this.id = id;
	}

	public Long getId() {
		return id;
	}

	public void setId(Long id) {
		this.id = id;
	}

	public String getValue() {
		return value;
	}

	public void setValue(String value) {
		this.value = value;
	}

}
